            <div class="program-detail" id="description">
                <h3>Program Description:</h3>
                <p>The Bachelor of Secondary Education aims to develop teachers with global competitiveness and develop critical thinking, as well as competency in research, leadership qualities, with creativity and innovation in the field of education. In line with this, our Bachelor of Secondary Education offer the following majors of study:</p>

                <h4>Major in English:</h4>
                <p>This program aims to develop skills in effective and creative writing, and enhance reading as well as listening and speaking skills for communicating in English and to develop ability for literary criticism and analysis, research and appreciation for literature and related studies for secondary education.</p>

                <h4>Major in Mathematics:</h4>
                <p>This program aims to develop critical and creative thinking for competent future teachers of mathematics in Secondary Education covering algebra, geometry, trigonometry, calculus, statistics and other related studies. It also aims to provide teaching skills that apply mathematics in the real world with its various modern applications and develop problem solving skills with global competitiveness.</p>

                <h4>Major in Sciences:</h4>
                <p>This program aims to develop technological, pedagogical, and content knowledge and skills in the fields of Sciences; Biology, Chemistry, Earth and Space, and Physics to prepare graduates to teach physical and biological sciences. It also aims to develop highly motivated and competent teachers for secondary science education with global competitiveness.</p>
            </div>

